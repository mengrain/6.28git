import {
	GET_USERINFO,
	GET_USER_PERM_STR_LIST,
	GET_ROUTE_NAME
} from './mutation-types.js'

export default {
	// 保存当前登录用户信息
	[GET_USERINFO](state, userInfo) {
		state.userInfo = userInfo;
	},
	// 保存当前登录用户权限
	[GET_USER_PERM_STR_LIST](state, permStrList) {
		state.permStrList = permStrList;
	},
	// 保存当前页面名称
	[GET_ROUTE_NAME](state, routeName) {
		state.routeName = routeName;
	},


}
